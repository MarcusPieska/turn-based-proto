#ifndef MAP_TILE_H
#define MAP_TILE_H

struct Point {
    int x;
    int y;
    Point () : x(0), y(0) {}
    Point (int x_val, int y_val) : x(x_val), y(y_val) {}
};

struct MapTile {
    Point pts[4];
    float z;
    MapTile () : z(0.0f) {}
    MapTile (Point top, Point right, Point bottom, Point left) : z(0.0f) {
        pts[0] = top;
        pts[1] = right;
        pts[2] = bottom;
        pts[3] = left;
    }
};

#endif // MAP_TILE_H

