#ifndef CITY_H
#define CITY_H

#include "game_primitives.h"

//================================================================================================================================
//=> - CityNetLinks -
//================================================================================================================================
//
//  Neighbor city index in each map quadrant (U16_KEY_NULL if none).
//
//================================================================================================================================

struct CityNetLinks {
    u16 m_ne; // Neighbor city NE (dx >= 0, dy < 0)
    u16 m_nw; // Neighbor city NW (dx < 0, dy <= 0)
    u16 m_se; // Neighbor city SE (dx > 0, dy >= 0)
    u16 m_sw; // Neighbor city SW (dx <= 0, dy > 0)
};

//================================================================================================================================
//=> - City class -
//================================================================================================================================
//
//  Exp stub: location, owner, and city-network adjacency. Full game City lives under city/city.h.
//
//================================================================================================================================

class alignas(8) City {
public:
    City ();
    ~City ();

    void init (u16 owner, u16 x, u16 y);

    u16 get_owner () const;
    u16 get_x () const;
    u16 get_y () const;
    CityNetLinks& links ();
    const CityNetLinks& links () const;
    void clr_net ();

private:
    u16 m_owner; // Owning seat index; U16_KEY_NULL until init
    u16 m_x; // Map column; U16_KEY_NULL until init
    u16 m_y; // Map row; U16_KEY_NULL until init
    CityNetLinks m_links; // Quadrant neighbor city indices
};

#endif // CITY_H

