#include "city_flags_array.h"

//================================================================================================================================
//=> - CityFlagsArray implementation -
//================================================================================================================================

CityFlagsArray::CityFlagsArray (u16 flag_count) :
    m_flags(flag_count) {
}

CityFlagsArray::~CityFlagsArray () {
}

bool CityFlagsArray::is_flagged (CityFlagsKey key) const {
    return m_flags.get_bit(key.value()) != 0;
}

void CityFlagsArray::set_flag (CityFlagsKey key) {
    m_flags.set_bit(key.value());
}

void CityFlagsArray::clear_flag (CityFlagsKey key) {
    m_flags.clear_bit(key.value());
}

