#include "civs_loaded_array.h"

//================================================================================================================================
//=> - CivsLoadedArray implementation -
//================================================================================================================================

CivsLoadedArray::CivsLoadedArray (u16 flag_count) :
    m_flags(flag_count) {
}

CivsLoadedArray::~CivsLoadedArray () {
}

bool CivsLoadedArray::is_flagged (CivsLoadedKey key) const {
    return m_flags.get_bit(key.value()) != 0;
}

void CivsLoadedArray::set_flag (CivsLoadedKey key) {
    m_flags.set_bit(key.value());
}

void CivsLoadedArray::clear_flag (CivsLoadedKey key) {
    m_flags.clear_bit(key.value());
}

