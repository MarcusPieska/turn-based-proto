#include "unit_type_static_data.h"

//================================================================================================================================
//=> - UnitTypeStaticData globals -
//================================================================================================================================

UnitTypeStaticDataStruct* UnitTypeStaticData::m_item_array = nullptr;
u16 UnitTypeStaticData::m_item_count = 0;

//================================================================================================================================
//=> - UnitTypeStaticData implementation -
//================================================================================================================================

void UnitTypeStaticData::set_items (UnitTypeStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const UnitTypeStaticDataStruct& UnitTypeStaticData::get_item (UnitTypeStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 UnitTypeStaticData::get_item_count () {
    return m_item_count;
}

