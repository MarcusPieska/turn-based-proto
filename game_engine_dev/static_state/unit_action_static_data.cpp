#include "unit_action_static_data.h"

//================================================================================================================================
//=> - UnitActionStaticData globals -
//================================================================================================================================

UnitActionStaticDataStruct* UnitActionStaticData::m_item_array = nullptr;
u16 UnitActionStaticData::m_item_count = 0;

//================================================================================================================================
//=> - UnitActionStaticData implementation -
//================================================================================================================================

void UnitActionStaticData::set_items (UnitActionStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const UnitActionStaticDataStruct& UnitActionStaticData::get_item (UnitActionStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 UnitActionStaticData::get_item_count () {
    return m_item_count;
}

