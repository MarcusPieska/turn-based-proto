#include "civ_static_data.h"

//================================================================================================================================
//=> - CivStaticData globals -
//================================================================================================================================

CivStaticDataStruct* CivStaticData::m_item_array = nullptr;
u16 CivStaticData::m_item_count = 0;

//================================================================================================================================
//=> - CivStaticData implementation -
//================================================================================================================================

void CivStaticData::set_items (CivStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const CivStaticDataStruct& CivStaticData::get_item (CivStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 CivStaticData::get_item_count () {
    return m_item_count;
}

