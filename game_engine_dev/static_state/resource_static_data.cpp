#include "resource_static_data.h"

//================================================================================================================================
//=> - ResourceStaticData globals -
//================================================================================================================================

ResourceStaticDataStruct* ResourceStaticData::m_item_array = nullptr;
u16 ResourceStaticData::m_item_count = 0;

//================================================================================================================================
//=> - ResourceStaticData implementation -
//================================================================================================================================

void ResourceStaticData::set_items (ResourceStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const ResourceStaticDataStruct& ResourceStaticData::get_item (ResourceStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 ResourceStaticData::get_item_count () {
    return m_item_count;
}

