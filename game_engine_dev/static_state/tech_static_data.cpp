#include "tech_static_data.h"

//================================================================================================================================
//=> - TechStaticData globals -
//================================================================================================================================

TechStaticDataStruct* TechStaticData::m_item_array = nullptr;
u16 TechStaticData::m_item_count = 0;

//================================================================================================================================
//=> - TechStaticData implementation -
//================================================================================================================================

void TechStaticData::set_items (TechStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const TechStaticDataStruct& TechStaticData::get_item (TechStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 TechStaticData::get_item_count () {
    return m_item_count;
}

