#include "civ_trait_static_data.h"

//================================================================================================================================
//=> - CivTraitStaticData globals -
//================================================================================================================================

CivTraitStaticDataStruct* CivTraitStaticData::m_item_array = nullptr;
u16 CivTraitStaticData::m_item_count = 0;

//================================================================================================================================
//=> - CivTraitStaticData implementation -
//================================================================================================================================

void CivTraitStaticData::set_items (CivTraitStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const CivTraitStaticDataStruct& CivTraitStaticData::get_item (CivTraitStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 CivTraitStaticData::get_item_count () {
    return m_item_count;
}

