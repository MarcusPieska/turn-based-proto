#include "city_flag_static_data.h"

//================================================================================================================================
//=> - CityFlagStaticData globals -
//================================================================================================================================

CityFlagStaticDataStruct* CityFlagStaticData::m_item_array = nullptr;
u16 CityFlagStaticData::m_item_count = 0;

//================================================================================================================================
//=> - CityFlagStaticData implementation -
//================================================================================================================================

void CityFlagStaticData::set_items (CityFlagStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const CityFlagStaticDataStruct& CityFlagStaticData::get_item (CityFlagStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 CityFlagStaticData::get_item_count () {
    return m_item_count;
}

