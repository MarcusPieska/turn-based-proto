//================================================================================================================================
//=> - CivVector implementation -
//================================================================================================================================

CivVector::CivVector()
    : m_civs(CivData::get_civ_data_count()) {
}

CivVector::~CivVector() {
}

bool CivVector::is_in_game(u16 civ_idx) const {
    return m_civs.get_bit(civ_idx) == 1;
}

void CivVector::add_to_game(u16 civ_idx) {
    m_civs.set_bit(civ_idx);
}

