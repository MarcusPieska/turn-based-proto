#include "building_static_data.h"

//================================================================================================================================
//=> - BuildingStaticData globals -
//================================================================================================================================

BuildingStaticDataStruct* BuildingStaticData::m_item_array = nullptr;
u16 BuildingStaticData::m_item_count = 0;

//================================================================================================================================
//=> - BuildingStaticData implementation -
//================================================================================================================================

void BuildingStaticData::set_items (BuildingStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const BuildingStaticDataStruct& BuildingStaticData::get_item (BuildingStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 BuildingStaticData::get_item_count () {
    return m_item_count;
}

