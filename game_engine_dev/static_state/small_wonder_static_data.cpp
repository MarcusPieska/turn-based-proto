#include "small_wonder_static_data.h"

//================================================================================================================================
//=> - SmallWonderStaticData globals -
//================================================================================================================================

SmallWonderStaticDataStruct* SmallWonderStaticData::m_item_array = nullptr;
u16 SmallWonderStaticData::m_item_count = 0;

//================================================================================================================================
//=> - SmallWonderStaticData implementation -
//================================================================================================================================

void SmallWonderStaticData::set_items (SmallWonderStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const SmallWonderStaticDataStruct& SmallWonderStaticData::get_item (SmallWonderStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 SmallWonderStaticData::get_item_count () {
    return m_item_count;
}

