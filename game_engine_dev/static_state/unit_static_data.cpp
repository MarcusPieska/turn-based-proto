#include "unit_static_data.h"

//================================================================================================================================
//=> - UnitStaticData globals -
//================================================================================================================================

UnitStaticDataStruct* UnitStaticData::m_item_array = nullptr;
u16 UnitStaticData::m_item_count = 0;

//================================================================================================================================
//=> - UnitStaticData implementation -
//================================================================================================================================

void UnitStaticData::set_items (UnitStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const UnitStaticDataStruct& UnitStaticData::get_item (UnitStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 UnitStaticData::get_item_count () {
    return m_item_count;
}

