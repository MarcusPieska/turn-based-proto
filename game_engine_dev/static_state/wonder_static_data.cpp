#include "wonder_static_data.h"

//================================================================================================================================
//=> - WonderStaticData globals -
//================================================================================================================================

WonderStaticDataStruct* WonderStaticData::m_item_array = nullptr;
u16 WonderStaticData::m_item_count = 0;

//================================================================================================================================
//=> - WonderStaticData implementation -
//================================================================================================================================

void WonderStaticData::set_items (WonderStaticDataStruct* items, u16 item_count) {
    m_item_array = items;
    m_item_count = item_count;
}

const WonderStaticDataStruct& WonderStaticData::get_item (WonderStaticDataKey key) {
    u16 idx = key.value();
    return m_item_array[idx];
}

u16 WonderStaticData::get_item_count () {
    return m_item_count;
}

