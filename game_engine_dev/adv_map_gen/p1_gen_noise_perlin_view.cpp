bool P1_Gen_NoisePerlinView::save_pri (cstr path, const u8* gray, u16 w, u16 h) {
    if (path == nullptr || gray == nullptr || w == 0 || h == 0) {
        return false;
    }
    MapArrayOverlay map;
    u8* raw = const_cast<u8*>(gray);
    if (!map.assign_copy(w, h, raw)) {
        return false;
    }
    return map.save(path);
}

