#ifndef P1_WB_UTIL_H
#define P1_WB_UTIL_H

#include "whiteboard_mng.h"

#include <cstdio>
#include <cstdlib>

//================================================================================================================================
//=> - P1 map-gen whiteboard helpers -
//================================================================================================================================

static inline void p1_wb_init (u16 w, u16 h, u16 slab_lim = 100u) {
    WhiteboardMng::init(w, h, slab_lim);
}

static inline void p1_wb_term () {
    WhiteboardMng::terminate();
}

static inline void p1_wb_fail (cstr cls, cstr fn) {
    std::fprintf(stderr, "P1 whiteboard checkout failed: %s %s\n", cls != nullptr ? cls : "", fn != nullptr ? fn : "");
    std::abort();
}

#define P1_WB_CHK(wb) do { if (!(wb).ok()) { p1_wb_fail((wb).cls(), (wb).fn()); } } while(0)

#endif // P1_WB_UTIL_H

