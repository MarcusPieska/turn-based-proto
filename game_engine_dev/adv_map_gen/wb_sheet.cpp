#include "wb_sheet.h"
#include "whiteboard.h"

//================================================================================================================================
//=> - WbSheet -
//================================================================================================================================

WbSheet::WbSheet (i32 tile_n) : m_p(Whiteboard::alloc(tile_n)) {}

WbSheet::~WbSheet () {
    Whiteboard::release(m_p);
}

u16* WbSheet::get () {
    return m_p;
}

u16 WbSheet::rd (u32 i) const {
    return m_p[i];
}

void WbSheet::wr (u32 i, u16 v) {
    m_p[i] = v;
}

bool WbSheet::ok () const {
    return m_p != nullptr;
}

