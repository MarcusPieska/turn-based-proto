#include "p1_adj_outline_fill_view.h"

#include "map_terrain_data.h"

//================================================================================================================================
//=> - P1_Adj_OutlineFillView -
//================================================================================================================================

bool P1_Adj_OutlineFillView::save_pri (cstr path, const u8* terrain, u16 w, u16 h) {
    if (path == nullptr || terrain == nullptr || w == 0 || h == 0) {
        return false;
    }
    MapTerrainData map;
    u8* raw = const_cast<u8*>(terrain);
    if (!map.assign_raw(w, h, raw)) {
        return false;
    }
    return map.save_terrain_ppm(path);
}

