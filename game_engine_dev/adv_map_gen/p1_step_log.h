#ifndef P1_STEP_LOG_H
#define P1_STEP_LOG_H

#include <cstdio>

//================================================================================================================================
//=> - P1 step abort logging -
//================================================================================================================================

static inline void p1_step_abort (cstr stage, cstr msg) {
    std::printf("%s abort: %s\n", stage, msg);
}

#define P1_STEP_ABORT(stage, msg) do { p1_step_abort((stage), (msg)); return false; } while(0)

#endif // P1_STEP_LOG_H

