#include "p1_tester_chain_core.h"

#include <ctime>

//================================================================================================================================
//=> - P1 tester chain core -
//================================================================================================================================

bool p1_build_chain_core (const P1_RunPrm& prm, u16 last_step, P1_MakeMapRslt* out, double* out_sec) {
    if (out == nullptr) {
        return false;
    }
    P1_MakeMap::free_rslt(out);
    const clock_t t0 = clock();
    P1_MakeMap mk(prm);
    if (!mk.generate(last_step) || !mk.is_valid()) {
        return false;
    }
    const clock_t t1 = clock();
    if (out_sec != nullptr) {
        *out_sec = static_cast<double>(t1 - t0) / static_cast<double>(CLOCKS_PER_SEC);
    }
    if (!P1_MakeMap::copy_rslt(out, mk.result())) {
        return false;
    }
    return true;
}

