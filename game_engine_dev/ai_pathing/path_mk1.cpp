PathMk1::PathMk1 () :
    m_x(nullptr),
    m_y(nullptr),
    m_n(0),
    m_cap(0) {}

PathMk1::~PathMk1 () {
    delete[] m_x;
    delete[] m_y;
}

void PathMk1::clr () {
    m_n = 0;
}

u16 PathMk1::n () const {
    return m_n;
}

u16 PathMk1::x (u16 i) const {
    return (m_x != nullptr && i < m_n) ? m_x[i] : 0u;
}

u16 PathMk1::y (u16 i) const {
    return (m_y != nullptr && i < m_n) ? m_y[i] : 0u;
}

bool PathMk1::push (u16 px, u16 py) {
    if (m_n >= m_cap) {
        const u32 nc = (m_cap == 0u) ? 64u : static_cast<u32>(m_cap) * 2u;
        if (nc > 65535u) {
            return false;
        }
        const u16 cap = static_cast<u16>(nc);
        u16* nx = new u16[cap];
        u16* ny = new u16[cap];
        if (nx == nullptr || ny == nullptr) {
            delete[] nx;
            delete[] ny;
            return false;
        }
        for (u16 i = 0; i < m_n; ++i) {
            nx[i] = m_x[i];
            ny[i] = m_y[i];
        }
        delete[] m_x;
        delete[] m_y;
        m_x = nx;
        m_y = ny;
        m_cap = cap;
    }
    m_x[m_n] = px;
    m_y[m_n] = py;
    ++m_n;
    return true;
}

