#include "assert_log.h"

#include <cstdio>

//================================================================================================================================
//=> - AssertLog implementation -
//================================================================================================================================

void AssertLog::log (cstr msg) {
    if (msg != nullptr) {
        std::fprintf(stderr, "GAME_EXPECT: %s\n", msg);
    } else {
        std::fprintf(stderr, "GAME_EXPECT\n");
    }
}

