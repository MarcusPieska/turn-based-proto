#ifndef STR_MNG_H
#define STR_MNG_H

#include <string>
#include <vector>
#include <iosfwd>

//================================================================================================================================
//=> - StringSplitter class -
//================================================================================================================================

class StringSplitter {
public:

    StringSplitter (const std::string& delimiter) : delimiter(delimiter) {}

    std::vector<std::string> split (const std::string& str) const;

private:
    std::string delimiter;
};

//================================================================================================================================
//=> - StringTrimmer class -
//================================================================================================================================

class StringTrimmer {
public:

    StringTrimmer (const std::string& trim_chars) : trim_chars(trim_chars) {}

    std::string trim (const std::string& str) const;

private:
    std::string trim_chars;
};

//================================================================================================================================
//=> - StringReader class -
//================================================================================================================================

class StringReader {
public:

    StringReader (const std::string& filename) : filename(filename) {}

    std::string read () const;

private:
    std::string filename;
};

//================================================================================================================================
//=> - StringWriter class -
//================================================================================================================================

class StringWriter {
public:

    StringWriter (const std::string& filename) : filename(filename) {}

    void append (const std::string& str);
    void write () const;

private:
    std::string filename;
    std::string content;
};

#endif // STR_MNG_H

