//================================================================================================================================
//=> - Climate RGB -
//================================================================================================================================

static inline void climate_to_rgb (u8 cls, u8* r, u8* g, u8* b) {
    if (cls == CLIMATE_GRASSLAND) {
        *r = 90;
        *g = 170;
        *b = 50;
        return;
    }
    if (cls == CLIMATE_PLAINS) {
        *r = 210;
        *g = 200;
        *b = 80;
        return;
    }
    if (cls == CLIMATE_DESERT) {
        *r = 210;
        *g = 160;
        *b = 70;
        return;
    }
    *r = 0;
    *g = 0;
    *b = 0;
}

static inline u8 climate_from_rgb (u8 r, u8 g, u8 b, bool* matched) {
    if (matched != nullptr) {
        *matched = false;
    }
    if (r == 90 && g == 170 && b == 50) {
        if (matched != nullptr) {
            *matched = true;
        }
        return CLIMATE_GRASSLAND;
    }
    if (r == 210 && g == 200 && b == 80) {
        if (matched != nullptr) {
            *matched = true;
        }
        return CLIMATE_PLAINS;
    }
    if (r == 210 && g == 160 && b == 70) {
        if (matched != nullptr) {
            *matched = true;
        }
        return CLIMATE_DESERT;
    }
    if (r == 0 && g == 0 && b == 0) {
        if (matched != nullptr) {
            *matched = true;
        }
        return CLIMATE_NONE;
    }
    return CLIMATE_NONE;
}

