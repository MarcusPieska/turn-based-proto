#include "linear_bld.h"

#include "bit_array.h"

//================================================================================================================================
//=> - LinearBld -
//================================================================================================================================

bool LinearBld::pick (const BitArrayCL& available, u16* out_idx) {
    if (out_idx == nullptr) {
        return false;
    }
    *out_idx = U16_KEY_NULL;
    const u32 n = available.get_count();
    for (u32 i = 0; i < n; ++i) {
        if (available.get_bit(i) != 0) {
            *out_idx = static_cast<u16>(i);
            return true;
        }
    }
    return false;
}

