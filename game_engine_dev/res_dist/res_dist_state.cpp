#include "res_dist_state.h"

#include <cstring>

//================================================================================================================================
//= - ResDistState -
//================================================================================================================================

ResDistState::ResDistState () : m_n(0) {
    for (u16 i = 0; i < RES_DIST_STATE_CAP; ++i) {
        m_cnt[i] = 0;
    }
}

bool ResDistState::reset (u16 res_n) {
    if (res_n > RES_DIST_STATE_CAP) {
        return false;
    }
    m_n = res_n;
    for (u16 i = 0; i < m_n; ++i) {
        m_cnt[i] = 0;
    }
    return true;
}

u16 ResDistState::res_n () const {
    return m_n;
}

u32 ResDistState::get (u16 res_i) const {
    if (res_i >= m_n) {
        return 0;
    }
    return m_cnt[res_i];
}

void ResDistState::add (u16 res_i, u32 n) {
    if (res_i >= m_n || n == 0) {
        return;
    }
    m_cnt[res_i] = m_cnt[res_i] + n;
}

bool ResDistState::met (u16 res_i, u32 target) const {
    return get(res_i) >= target;
}

