#ifndef RESOURCE_TYPES_H
#define RESOURCE_TYPES_H

#include "game_primitives.h"
#include "game_map_defs.h"

//================================================================================================================================
//=> - Resource IO limits -
//================================================================================================================================

#define RES_IO_NAME_MAX 32
#define RES_IO_TECH_MAX 48
#define RES_IO_QUAD_MAX 4
#define RES_IO_ENTRY_MAX 64

#define RES_OV_ALL 200
#define RES_OV_NONE 201
#define RES_OV_SWAMPS 1
#define RES_OV_FORESTS 2
#define RES_OV_JUNGLES 3
#define RES_OV_RIVERS 4

#define RES_TERR_ALL 250
#define RES_CLIM_ALL 251

#define CLIMATE_TUNDRA 4

//================================================================================================================================
//=> - ResQuad -
//================================================================================================================================

typedef struct ResQuad {
    u8 m_terr;
    u8 m_clim;
    u8 m_ov;
    u16 m_wt;
} ResQuad;

//================================================================================================================================
//=> - ResPlacement -
//================================================================================================================================

typedef struct ResPlacement {
    u16 m_res_wt;
    u8 m_quad_n;
    ResQuad m_quads[RES_IO_QUAD_MAX];
} ResPlacement;

//================================================================================================================================
//=> - ResEntry -
//================================================================================================================================

typedef struct ResEntry {
    char m_name[RES_IO_NAME_MAX];
    u16 m_food;
    u16 m_shields;
    u16 m_commerce;
    char m_tech[RES_IO_TECH_MAX];
    u8 m_has_plc;
    ResPlacement m_plc;
} ResEntry;

//================================================================================================================================
//=> - ResCatalog -
//================================================================================================================================

typedef struct ResCatalog {
    u16 m_n;
    ResEntry* m_items;
} ResCatalog;

#endif // RESOURCE_TYPES_H

