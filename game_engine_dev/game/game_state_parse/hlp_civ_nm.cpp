#include "hlp_civ_nm.h"

#include "civ_static_key.h"
#include "runtime_statics.h"

//================================================================================================================================
//=> - Helper_CivNm -
//================================================================================================================================

cstr Helper_CivNm::nm (const RuntimeStatics& st, u16 civ_idx) {
    if (civ_idx >= st.civ().get_item_count()) {
        return "?";
    }
    cstr nm = st.civ().get_name(CivStaticDataKey::from_raw(civ_idx));
    return (nm != nullptr) ? nm : "?";
}

