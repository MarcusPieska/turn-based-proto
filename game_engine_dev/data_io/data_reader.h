#ifndef DATA_READER_H
#define DATA_READER_H

#include <string>
#include <vector>

//================================================================================================================================
//=> - RawItem struct -
//================================================================================================================================

struct RawItem {
    std::string name;
    std::string raw_line;
    
    RawItem () : name("NONE"), raw_line("NONE") {}
};

//================================================================================================================================
//=> - DataReader class -
//================================================================================================================================

class DataReader {
public:
    explicit DataReader (const std::string& filepath);

    const std::vector<RawItem>& get_raw_items () const;

private:
    DataReader () = delete;
    DataReader (const DataReader& other) = delete;
    DataReader (DataReader&& other) = delete;

    void read_and_parse ();

    std::string m_filepath;
    std::vector<RawItem> m_raw_items;
};

#endif // DATA_READER_H

