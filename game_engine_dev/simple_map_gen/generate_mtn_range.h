struct MtnRangePt {
    u16 x;
    u16 y;
};

//================================================================================================================================
//=> - MtnRangeResult -
//================================================================================================================================

struct MtnRangeResult {
    MtnRangePt* pts;
    u32 pt_n;
};

//================================================================================================================================
//=> - Generate_MtnRange -
//================================================================================================================================

class Generate_MtnRange {
public:
    static MtnRangeResult* generate (
        const u16* dist,
        u16 w,
        u16 h,
        i32 coverage_perc,
        i32 target_depth,
        u16 sx,
        u16 sy);
    static void free_result (MtnRangeResult* res);

private:
    Generate_MtnRange () = delete;
    Generate_MtnRange (const Generate_MtnRange& other) = delete;
    Generate_MtnRange (Generate_MtnRange&& other) = delete;
};

