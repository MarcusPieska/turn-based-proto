#ifndef GENERATE_MAP_CLIMATE_H
#define GENERATE_MAP_CLIMATE_H

#include "game_primitives.h"
#include "game_map_defs.h"

//================================================================================================================================
//=> - MapClimateOverlayWts -
//================================================================================================================================

struct MapClimateOverlayWts {
    u8 w_dist_river;
    u8 w_open_dist_water;
    u8 w_latitude;
    u8 w_plain_dist_water;
};

//================================================================================================================================
//=> - MapClimateTypePct -
//================================================================================================================================

struct MapClimateTypePct {
    u8 pct_grassland;
    u8 pct_plains;
    u8 pct_desert;
};

//================================================================================================================================
//=> - MapClimateParams -
//================================================================================================================================

struct MapClimateParams {
    MapClimateOverlayWts wts;
    MapClimateTypePct pct;
};

//================================================================================================================================
//=> - MapClimateOverlays -
//================================================================================================================================

struct MapClimateOverlays {
    const u8* dist_river;
    const u8* open_dist_water;
    const u8* latitude;
    const u8* plain_dist_water;
};

//================================================================================================================================
//=> - MapClimateTileVal -
//================================================================================================================================

struct MapClimateTileVal {
    u32 val;
    u16 x;
    u16 y;
};

//================================================================================================================================
//=> - MapClimateResult -
//================================================================================================================================

struct MapClimateResult {
    u16 w;
    u16 h;
    u8* climate;
    MapClimateTileVal* tiles;
    u32 tile_n;
};

//================================================================================================================================
//=> - Generate_MapClimate -
//================================================================================================================================

class Generate_MapClimate {
public:
    static MapClimateResult* generate (
        const u8* terrain,
        u16 w,
        u16 h,
        const MapClimateOverlays* overlays,
        const MapClimateParams* params);
    static void free_result (MapClimateResult* res);

private:
    Generate_MapClimate () = delete;
    Generate_MapClimate (const Generate_MapClimate& other) = delete;
    Generate_MapClimate (Generate_MapClimate&& other) = delete;
};

#endif // GENERATE_MAP_CLIMATE_H

