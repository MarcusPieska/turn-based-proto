import sys
sys.dont_write_bytecode = True

import os

#================================================================================================================================#
#=> - functions -
#================================================================================================================================#

def run_map_gen_dev(argv):
    d = os.path.dirname(os.path.abspath(__file__))
    if d not in sys.path:
        sys.path.insert(0, d)
    import dev_small_shape_mk1_gui as gui
    return gui.run_app(argv)

#================================================================================================================================#
#=> - Main -
#================================================================================================================================#

if __name__ == "__main__":
    raise SystemExit(run_map_gen_dev(sys.argv[1:]))

