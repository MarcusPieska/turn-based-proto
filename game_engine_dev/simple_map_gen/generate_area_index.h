#ifndef GENERATE_AREA_INDEX_H
#define GENERATE_AREA_INDEX_H

#include "game_primitives.h"

//================================================================================================================================
//=> - AreaIdxEntry -
//================================================================================================================================

struct AreaIdxEntry {
    u16 x;
    u16 y;
    u32 size;
};

//================================================================================================================================
//=> - AreaIdxResult -
//================================================================================================================================

struct AreaIdxResult {
    AreaIdxEntry* areas;
    u32 area_n;
};

//================================================================================================================================
//=> - Generate_AreaIndex -
//================================================================================================================================

class Generate_AreaIndex {
public:
    static AreaIdxResult* generate_eq (const u8* terrain, u16 w, u16 h, u8 terr_idx);
    static AreaIdxResult* generate_ge (const u8* terrain, u16 w, u16 h, u8 terr_idx);
    static AreaIdxResult* generate_le (const u8* terrain, u16 w, u16 h, u8 terr_idx);
    static void free_result (AreaIdxResult* res);

private:
    Generate_AreaIndex () = delete;
    Generate_AreaIndex (const Generate_AreaIndex& other) = delete;
    Generate_AreaIndex (Generate_AreaIndex&& other) = delete;
};

#endif // GENERATE_AREA_INDEX_H

