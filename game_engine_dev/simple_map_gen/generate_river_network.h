#define RIVER_SYS_NONE 0xFFFFu

//================================================================================================================================
//=> - RiverConn -
//================================================================================================================================

struct RiverConn {
    u16 a;
    u16 b;
};

//================================================================================================================================
//=> - RiverNetworkResult -
//================================================================================================================================

struct RiverNetworkResult {
    u16 sector_n;
    u16 conn_n;
    RiverConn* conns;
    u16* river_sys;
    bool* coastal;
};

//================================================================================================================================
//=> - Generate_RiverNetwork -
//================================================================================================================================

class Generate_RiverNetwork {
public:
    static RiverNetworkResult* generate (
        const u8* terrain,
        u16 w,
        u16 h,
        const RiverSectorsResult* sectors);
    static void free_result (RiverNetworkResult* res);

private:
    Generate_RiverNetwork () = delete;
    Generate_RiverNetwork (const Generate_RiverNetwork& other) = delete;
    Generate_RiverNetwork (Generate_RiverNetwork&& other) = delete;
};

