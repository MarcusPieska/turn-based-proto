#ifndef GENERATE_RIVER_NETWORK_H
#define GENERATE_RIVER_NETWORK_H

#include "game_primitives.h"
#include "generate_river_sectors.h"

//================================================================================================================================
//=> - Limits -
//================================================================================================================================

#define RIVER_SYS_NONE 0xFFFFu
#define RIVER_BASIN_NONE 0u

//================================================================================================================================
//=> - RiverConn -
//================================================================================================================================

struct RiverConn {
    u16 a;
    u16 b;
};

//================================================================================================================================
//=> - RiverBasinEntry -
//================================================================================================================================

struct RiverBasinEntry {
    u16 idx;
    u16 mouth_x;
    u16 mouth_y;
    u32 tile_n;
};

//================================================================================================================================
//=> - RiverNetworkResult -
//================================================================================================================================

struct RiverNetworkResult {
    u16 w;
    u16 h;
    u16 sector_n;
    u16 conn_n;
    u16 basin_n;
    RiverConn* conns;
    u16* river_sys;
    bool* coastal;
    u16* overlay;
    RiverBasinEntry* basins;
};

//================================================================================================================================
//=> - Generate_RiverNetwork -
//================================================================================================================================

class Generate_RiverNetwork {
public:
    static RiverNetworkResult* generate (const u8* terrain, u16 w, u16 h, const RiverSectorsResult* sectors);
    static void free_result (RiverNetworkResult* res);

private:
    Generate_RiverNetwork () = delete;
    Generate_RiverNetwork (const Generate_RiverNetwork& other) = delete;
    Generate_RiverNetwork (Generate_RiverNetwork&& other) = delete;
};

#endif // GENERATE_RIVER_NETWORK_H

