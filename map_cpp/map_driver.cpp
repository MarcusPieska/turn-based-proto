#include "map_tiler.h"
#include "map_model.h"
#include "map_view.h"
#include <iostream>

//================================================================================================================================
//=> - Main -
//================================================================================================================================

int main () {
    int m_width = 6050;
    int m_height = 6050;
    int t_width = 100;
    int t_height = 60;
    int w_width = 1600;
    int w_height = 800;
    
    MapModel model;
    MapTiler tiler (m_width, m_height, t_width, t_height, &model);
    
    model.saveTilesToFile ("tiles.dat");
    
    MapView view (w_width, w_height, m_width, m_height, &model);
    if (!view.initialize ()) {
        return 1;
    }
    
    while (view.isRunning ()) {
        view.handleEvents ();
        view.update ();
        view.render_opt ();
        SDL_Delay (16);
    }
    
    return 0;
}

