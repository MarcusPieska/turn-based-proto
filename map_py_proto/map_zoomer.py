class MapZoomer:

    def __init__ (m, model):
        m.model = model
        m.zoom_level = 1.0
        m.tiles, m.lines = [], []
    
    def zoom_with_factor (m, factor):
        m.tiles = m.model.get_tiles_deep_copy ()
        for tile_row in m.tiles:
            for tile in tile_row:
                tile.corners = tuple((x * factor, y * factor) for x, y in tile.corners)
        m.lines = []
        for line in m.model.get_lines_deep_copy ():
            pt1 = (line[0][0] * factor, line[0][1] * factor)
            pt2 = (line[1][0] * factor, line[1][1] * factor)
            m.lines.append((pt1, pt2))

    def get_zoomed_tiles (m):
        return m.tiles

    def get_zoomed_lines (m):
        return m.lines

